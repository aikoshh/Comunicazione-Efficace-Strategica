import { Language } from '../types';
import { MODULES_IT, STRATEGIC_CHECKUP_EXERCISES_IT, VOICE_RUBRIC_CRITERIA_IT, QUESTION_LIBRARY_IT, PREPARATION_CHECKLIST_IT } from './content.it';
import { MODULES_EN, STRATEGIC_CHECKUP_EXERCISES_EN, VOICE_RUBRIC_CRITERIA_EN, QUESTION_LIBRARY_EN, PREPARATION_CHECKLIST_EN } from './content.en';
import { translations } from './translations';

export const getContent = (lang: Language) => {
  if (lang === 'en') {
    return {
      MODULES: MODULES_EN,
      STRATEGIC_CHECKUP_EXERCISES: STRATEGIC_CHECKUP_EXERCISES_EN,
      VOICE_RUBRIC_CRITERIA: VOICE_RUBRIC_CRITERIA_EN,
      QUESTION_LIBRARY: QUESTION_LIBRARY_EN,
      PREPARATION_CHECKLIST: PREPARATION_CHECKLIST_EN,
      GEMINI: translations.en.gemini,
    };
  }
  // Default to Italian
  return {
    MODULES: MODULES_IT,
    STRATEGIC_CHECKUP_EXERCISES: STRATEGIC_CHECKUP_EXERCISES_IT,
    VOICE_RUBRIC_CRITERIA: VOICE_RUBRIC_CRITERIA_IT,
    QUESTION_LIBRARY: QUESTION_LIBRARY_IT,
    PREPARATION_CHECKLIST: PREPARATION_CHECKLIST_IT,
    GEMINI: translations.it.gemini,
  };
};
